export type LessonId = keyof typeof lessonTranslations;
export type SupportedLanguage = 'en' | 'es' | 'fr' | 'hi';

export function getTranslatedLesson(lessonId: string, language: SupportedLanguage) {
  const translations = lessonTranslations[lessonId as LessonId];
  if (!translations) return null;
  return translations[language] || translations.en;
}
